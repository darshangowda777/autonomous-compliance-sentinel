# -----------------------------

st.markdown('<div class="main-title">🛡 AI Compliance Sentinel</div>', unsafe_allow_html=True)

st.write("Analyze AI project proposals and automatically generate compliance reports.")

st.divider()

# -----------------------------
# EXAMPLE LOADER
# -----------------------------

example_text = """
The Human Resources department proposes an AI system that automatically ranks job applicants based on historical hiring data and behavioral indicators extracted from video interviews.
"""

if st.button("Load Example Proposal", key="load_example"):
    st.session_state["proposal"] = example_text

# -----------------------------
# USER INPUT
# -----------------------------

title = st.text_input("Proposal Title")

proposal = st.text_area(
    "Proposal Description",
    value=st.session_state.get("proposal",""),
    height=200
)

st.divider()

# -----------------------------
# ANALYSIS BUTTON
# -----------------------------

if st.button("Analyze Proposal", key="analyze"):

    if proposal.strip() == "":
        st.warning("Please enter a proposal description")

    else:

        vec = vectorizer.transform([proposal])

        prediction = svm_model.predict(vec)[0]

        score = svm_model.decision_function(vec)[0]

        action = "Review Required" if prediction == "red_flag" else "Approved"

        st.subheader("Compliance Analysis")

        col1, col2, col3 = st.columns(3)

        with col1:
            st.markdown(
                f'<div class="metric-box"><b>Prediction</b><br>{prediction.upper()}</div>',
                unsafe_allow_html=True
            )

        with col2:
            st.markdown(
                f'<div class="metric-box"><b>Explanation Score</b><br>{round(score,3)}</div>',
                unsafe_allow_html=True
            )

        with col3:
            st.markdown(
                f'<div class="metric-box"><b>Compliance Action</b><br>{action}</div>',
                unsafe_allow_html=True
            )

        st.divider()

        # -----------------------------
        # RISK DISPLAY
        # -----------------------------

        if prediction == "red_flag":
            st.markdown('<div class="risk-box">⚠ High Risk Detected</div>', unsafe_allow_html=True)
        else:
            st.markdown('<div class="clean-box">✅ Proposal Appears Compliant</div>', unsafe_allow_html=True)

        

        # -----------------------------
        # CREATE JIRA ISSUE
        # -----------------------------

        issue_key = create_jira_issue(title, proposal)

        if issue_key:

            st.success(f"Jira Issue Created: {issue_key}")

            update_jira_with_result(issue_key, prediction, score, action)

            st.success("Compliance result stored in Jira")


        else:

            st.error("Failed to create Jira issue")

st.divider()

st.caption("AI Compliance Sentinel – AI Governance Prototype")