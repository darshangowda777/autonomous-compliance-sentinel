import streamlit as st
import pickle
import requests
from requests.auth import HTTPBasicAuth

# -----------------------------
# LOAD ML MODEL
# -----------------------------

svm_model = pickle.load(open("svm_model.pkl", "rb"))
vectorizer = pickle.load(open("vectorizer.pkl", "rb"))

# -----------------------------
# JIRA CONFIG
# -----------------------------

EMAIL = "YOUR_MAIL"
API_TOKEN = "YOUR_API_TOKEN"
BASE_URL = "https://compliance-sentinel-demo1.atlassian.net"

PROJECT_KEY = "KAN"   # change if your Jira project key is different

auth = HTTPBasicAuth(EMAIL, API_TOKEN)

headers = {
    "Accept": "application/json",
    "Content-Type": "application/json"
}

# -----------------------------
# CREATE JIRA ISSUE
# -----------------------------

def create_jira_issue(title, description):

    url = f"{BASE_URL}/rest/api/3/issue"

    payload = {
        "fields": {
            "project": {
                "key": PROJECT_KEY
            },
            "summary": title,
            "description": {
                "type": "doc",
                "version": 1,
                "content": [{
                    "type": "paragraph",
                    "content": [{
                        "type": "text",
                        "text": description
                    }]
                }]
            },
            "issuetype": {
                "name": "Task"
            }
        }
    }

    response = requests.post(
        url,
        headers=headers,
        auth=auth,
        json=payload
    )

    if response.status_code == 201:
        return response.json()["key"]
    else:
        return None


# -----------------------------
# UPDATE JIRA WITH RESULT
# -----------------------------

def update_jira_with_result(issue_key, prediction, score, action):

    comment = f"""
AI Compliance Analysis Result

Risk Prediction: {prediction}

Explanation Score: {round(score,3)}

Compliance Action: {action}

Generated by AI Compliance Sentinel
"""

    url = f"{BASE_URL}/rest/api/3/issue/{issue_key}/comment"

    payload = {
        "body": {
            "type": "doc",
            "version": 1,
            "content": [{
                "type": "paragraph",
                "content": [{
                    "type": "text",
                    "text": comment
                }]
            }]
        }
    }

    requests.post(
        url,
        headers=headers,
        auth=auth,
        json=payload
    )


